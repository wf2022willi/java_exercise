public class variabletief{
	
	public static void main (String[] args){
		byte b = 10;
		System.out.println(b);
		short s = 20;
		System.out.println(s);
		
		long j = 999999999L;// must type "L" when you define a long variable
		System.out.println(j);
		
		float f = 10.223F;
		System.out.println(f);// must type "F" when you define a FLOAT variable
		
		//boolean
		boolean o = true;
		System.out.println(o);
		
	}
	
	
	
}













